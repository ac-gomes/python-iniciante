# O nome de uma variavel em Python mão pode iniciar com números
# Para atribuir um avalor a uma varivel deve-se, primeiro definir um nome valido para a variavel,
# o nome sempre estará a esquerda do sinal de = e a direita  o valore será armazenado na variavel.
# Python não é uma linguagem fortimente tipada, isso significa que o interpretador se encarrega
# em identificar o tipo de valor armazenado em uma variavel.

# A variavel 'a' recebe o valor 20.
a = 20

# Imprimir o valor de a.
print(a)

# A variavel 'b' recebe o valor 5.5
b = 5.5
print(b)

# A variavel 'fruta' recebe o valor 'Manga'
fruta = 'manga'
# Imprimir valor de fruta
print(fruta)

# Variavel verdadeiro recebe true
verdadeiro = True

# Imprimindo o valor de verdadeiro
print(verdadeiro)
